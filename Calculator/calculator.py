def add(input_1,input_2):
    result = input_1 + input_2
    return result

def subtract(input_1,input_2):
    result = input_1 - input_2
    return result

def multiply(input_1,input_2):
    result = input_1 * input_2
    return result

def divide(input_1,input_2):
    result = input_1 / input_2
    return result
